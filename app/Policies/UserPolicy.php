    /**
     * Determine whether the user can delete the model.
     */
    public function delete(User $user, User $model): bool
    {
        // If the auth user is content moderator they can always delete the user unless it's themselves
        if ($user->hasRole('content moderator') && $user->id != $model->id)
            return true;

        // If the auth user want to remove their own profile
        if ($user->id == $model->id) {
            if ($user->speaker)
                return !$user->speaker->is_approved; // If they are not approved as a speaker already, they can delete their account
        }

        return false;
    }

